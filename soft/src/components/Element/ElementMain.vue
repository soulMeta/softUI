<script>
  import $ from "jquery"
    export default {
      name: "ElementMain",
      methods:{
        blocknow(){
          //一：点击侧边栏改变左侧盒子距离页面的高度
          let lia = $(".right-first-lia");
          let divbox = $(".row-left-box");
          lia.each(function(i){
            $(this).click(function(){
              let boxtop = divbox.eq(i).offset().top;
              $(window).scrollTop(boxtop+2);
            })
          });

          $(".right-second-ul").each(function(i){
            $(this).children().each(function(j){
              $(this).click(function(){
                let topnow = divbox.eq(i).children(".button-box").eq(j).offset().top-40;
                $(window).scrollTop(topnow);
              })
            });
          });
        }
      },
      mounted(){
        $(".left-top-erweima-little").mouseenter(function(){
          $(".left-top-erweima-big").show();
        });
        $(".left-top-erweima-little").mouseleave(function(){
          $(".left-top-erweima-big").hide();
        });
        let righttop = $(".row-right").offset().top;

        let lia = $(".right-first-lia");
        let divtoparr = [];
        let buttontoparr = [];
        let divbox = $(".row-left-box");
        let bodyheight = document.body.clientHeight;
        divbox.each(function(){
          let divboxtop = $(this).offset().top;
          divtoparr.push(divboxtop);
        })
        divtoparr.push(bodyheight);

        divbox.children(".button-box").each(function(){
          let buttontop = $(this).offset().top;
          buttontoparr.push(buttontop);
        })
        buttontoparr.push(bodyheight);

        window.onscroll=function(){
          let top = $(window).scrollTop();
          if(top<righttop){
            $(".row-right").css({
              "width":"31%",
              "height":"500px",
              "float":"left",
              "padding":"2% 0 0 6.7%",
              "background-color": "white",
              "font-size": "15px",
              "position": "",
            });
          }else if(top>=righttop){
            $(".row-right").css({
              "position": "fixed",
              "right": "0",
              "top": "0",
            });
          }
          if(top>20 && top<divbox.eq(1).offset().top-10){
            $(".right-second-ul").eq(0).show();
            $(".right-first-lia").eq(0).css({
              "color": "#12B7F5",
              "border-left-color": "#12B7F5",
            });
            $(".right-second-ul").eq(0).children().eq(0).css({
              "color": "#12B7F5",
              "border-left-color": "#12B7F5",
            })
          }
          if(top>divbox.eq(0).children(".button-box").eq(1).offset().top-1){
            $(".right-second-ul").eq(0).children().eq(0).css({
              "color": "#777777",
              "border-left-color": "transparent",
            })
          }

          for(let i=0;i<divtoparr.length;i++){
            if(top>=divtoparr[i] && top<=divtoparr[i+1]){
              lia.css({
                "color": "#777777",
                "border-left-color": "transparent",
              })
              lia.eq(i).css({
                "color": "#12B7F5",
                "border-left-color": "#12B7F5",
              })
              lia.eq(i).next().children().css({
                "color": "#777777",
                "border-left-color": "transparent",
              });
              lia.eq(i).next().children().eq(0).css({
                "color": "#12B7F5",
                "border-left-color": "#12B7F5",
              });
              lia.next().hide();
              lia.eq(i).next().show();
            }
          }
          for(let j=0;j<buttontoparr.length;j++){
            if(top>=buttontoparr[j]-120 && top<=buttontoparr[j+1]){
              lia.next().children().css({
                "color": "#777777",
                "border-left-color": "transparent",
              });
              lia.next().children().find("a").css({
                "color": "#777777",
                "border-left-color": "transparent",
              });
              lia.next().children().eq(j).css({
                "color": "#12B7F5",
                "border-left-color": "#12B7F5",
              });
              
              lia.next().children().eq(j).find("a").css({
                "color": "#12B7F5",
                "border-left-color": "#12B7F5",
              });
            }
          }
        };
        this.blocknow();
      }
    }
</script>

